char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char			*temp_dest;

	temp_dest = dest;
	while (n > 0 && *src != '\0')
	{
		*dest++ = *src++;
		n--;
	}
	while (n > 0)
	{
		*dest++ = '\0';
		n--;
	}
	return (temp_dest);
}
